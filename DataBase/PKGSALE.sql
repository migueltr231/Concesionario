    PROCEDURE SP_REGISTER_SALE(
        P_SALE_ID           OUT TBL_SALE.SALE_ID%TYPE,
        P_CUS_ID            IN TBL_SALE.CUS_ID%TYPE,
        P_EMP_ID            IN TBL_SALE.EMP_ID%TYPE,
        P_UNIT_ID            IN TBL_SALE.UNIT_ID%TYPE,
        P_SALE_PRICE        IN TBL_SALE.SALE_PRICE%TYPE
    );

    PROCEDURE SP_REGISTER_RESERVATION(
        P_SALE_ID           OUT TBL_SALE.SALE_ID%TYPE,
        P_CUS_ID            IN TBL_SALE.CUS_ID%TYPE,
        P_EMP_ID            IN TBL_SALE.EMP_ID%TYPE,
        P_UNIT_ID            IN TBL_SALE.UNIT_ID%TYPE,
        P_SALE_PRICE        IN TBL_SALE.SALE_PRICE%TYPE,
        P_SALE_DATE_END     IN TBL_SALE.SALE_DATE_END%TYPE DEFAULT NULL
